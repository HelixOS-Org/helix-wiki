interface I18nContextType {
  locale: Locale;
  setLocale: (l: Locale) => void;
  t: (key: string) => string;
  dir: "ltr" | "rtl";
}

const I18nContext = createContext<I18nContextType>({
  locale: "en",
  setLocale: () => {},
  t: (key) => key,
  dir: "ltr",
});

const STORAGE_KEY = "helix-wiki-locale";

/** Detect the best matching locale from the browser */
function detectLocale(): Locale {
  if (typeof window === "undefined") return "en";
  // Check localStorage first
  const stored = localStorage.getItem(STORAGE_KEY);
  if (stored && LOCALES.some((l) => l.code === stored)) return stored as Locale;
  // Check browser language
  const nav = navigator.language || (navigator as unknown as { userLanguage?: string }).userLanguage || "en";
  const primary = nav.split("-")[0].toLowerCase();
  if (LOCALES.some((l) => l.code === primary)) return primary as Locale;
  return "en";
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("en");
  const [mounted, setMounted] = useState(false);

  // Auto-detect on mount
  useEffect(() => {
    setLocaleState(detectLocale());
    setMounted(true);
  }, []);

  const setLocale = useCallback((l: Locale) => {
    setLocaleState(l);
    if (typeof window !== "undefined") {
      localStorage.setItem(STORAGE_KEY, l);
      // Update html lang attribute
      document.documentElement.lang = l;
      // Update dir for RTL
      const info = LOCALES.find((loc) => loc.code === l);
      document.documentElement.dir = info?.dir || "ltr";
    }
  }, []);

  const t = useCallback(
    (key: string): string => {
      const dict = translations[locale];
      return dict?.[key] ?? translations.en[key] ?? key;
    },
    [locale],
  );

  const dir = LOCALES.find((l) => l.code === locale)?.dir || "ltr";

  // Prevent hydration mismatch â€” render with "en" until mounted
  const value: I18nContextType = {
    locale: mounted ? locale : "en",
    setLocale,
    t: mounted ? t : (key: string) => translations.en[key] ?? key,
    dir: mounted ? dir : "ltr",
  };

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}

